def create_comprehensive_material_database():
    """创建综合材料数据库"""
    print("🏗️ 正在生成综合材料数据库...")
    
    # 获取三类材料数据
    single_sided = generate_single_sided_tape_data()
    double_sided = generate_double_sided_tape_data()
    protective_film = generate_protective_film_data()
    
    # 合并数据
    all_data = single_sided + double_sided + protective_film
    
    # 创建DataFrame
    df = pd.DataFrame(all_data)
    
    # 生成时间戳
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    
    # 保存到Excel
    output_file = f"模切材料数据库_{timestamp}.xlsx"
    df.to_excel(output_file, index=False, engine='openpyxl')
    
    print(f"✅ 材料数据库生成完成!")
    print(f"📊 总计: {len(all_data)} 个材料信息")
    print(f"   - 单面胶: {len(single_sided)} 个")
    print(f"   - 双面胶: {len(double_sided)} 个") 
    print(f"   - 保护膜: {len(protective_film)} 个")
    print(f"💾 文件保存为: {output_file}")
    
    # 显示数据预览
